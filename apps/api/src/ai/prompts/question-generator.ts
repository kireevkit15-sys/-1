/**
 * Prompt builder for batch question generation aligned with RAZUM taxonomy.
 *
 * Usage:
 *   const prompt = buildQuestionGeneratorPrompt({ ... });
 *   // pass `prompt` as the user message to Claude Sonnet
 */

// ── Difficulty descriptors (Bloom's taxonomy aligned) ──────────────────

const DIFFICULTY_RULES: Record<string, string> = {
  BRONZE: `Уровень BRONZE (определения и узнавание).
- Проверяет знание базовых определений, терминов, простых концепций.
- Правильный ответ очевиден для человека, знакомого с темой.
- Дистракторы содержат распространённые заблуждения и подмену понятий.
- Формулировка вопроса прямая: «Что такое...», «Какой термин описывает...», «Что из перечисленного верно...».`,

  SILVER: `Уровень SILVER (применение и анализ).
- Вопрос описывает конкретную жизненную ситуацию и требует выбрать правильное применение концепции.
- Нужно не просто знать определение, а уметь перенести теорию на практику.
- Дистракторы — правдоподобные, но ошибочные стратегии действия.
- Формулировка: «В ситуации X, какое действие соответствует принципу Y?», «Какой подход будет наиболее эффективным...»`,

  GOLD: `Уровень GOLD (синтез и оценка).
- Вопрос требует синтеза нескольких концепций или распознавания нескольких ошибок одновременно.
- Может содержать кейс с неочевидным правильным ответом, где нужна глубокая экспертиза.
- Дистракторы — частично верные утверждения, ловушки для поверхностного мышления.
- Формулировка: «Какая комбинация принципов...», «Что является корневой причиной...», «Какой подход интегрирует...»`,
};

// ── Branch descriptors ─────────────────────────────────────────────────

const BRANCH_DESC: Record<string, string> = {
  STRATEGY:
    'Стратегия — принятие решений, планирование, управление ресурсами, целеполагание, переговоры, лидерство, финансовое мышление.',
  LOGIC:
    'Логика — критическое мышление, когнитивные искажения, аргументация, формальная и неформальная логика, анализ данных, научный метод.',
  ERUDITION:
    'Эрудиция — знания о мире, наука, история, философия, психология, экономика, культура.',
  RHETORIC:
    'Риторика — коммуникация, аргументация, переговоры, публичные выступления, сторителлинг, убеждение.',
  INTUITION:
    'Интуиция — распознавание паттернов, вероятности, когнитивные искажения, эмоциональный интеллект, быстрое мышление.',
};

// ── Stat mapping guide ─────────────────────────────────────────────────

const STAT_GUIDE = `Характеристики аватара (для statPrimary / statSecondary):
- "LOGIC" — логическое мышление, анализ, дедукция
- "ERUDITION" — широта знаний, факты, межпредметные связи
- "STRATEGY" — планирование, принятие решений, управление
- "RHETORIC" — аргументация, убеждение, коммуникация
- "INTUITION" — распознавание паттернов, эвристики, быстрые оценки

statPrimary — основная характеристика, которую развивает вопрос.
statSecondary — дополнительная характеристика (необязательно). Указывай, только если вопрос явно затрагивает вторую область.`;

// ── Exported interface ─────────────────────────────────────────────────

export interface QuestionGeneratorParams {
  /** Категория из таксономии (рус.), e.g. "Когнитивные искажения" */
  categoryRu: string;
  /** Подкатегория из таксономии (рус.), e.g. "Искажения памяти" */
  subcategoryRu: string;
  /** Конкретная тема из таксономии (рус.), e.g. "Эффект ложной памяти" */
  topicRu: string;
  /** Ветка дерева знаний */
  branch: 'STRATEGY' | 'LOGIC' | 'ERUDITION' | 'RHETORIC' | 'INTUITION';
  /** Уровень сложности */
  difficulty: 'BRONZE' | 'SILVER' | 'GOLD';
  /** Количество вопросов для генерации (рекомендуется 5-20) */
  count: number;
  /** Тексты уже существующих вопросов по данной теме — для антидубликации */
  existingQuestions?: string[];
  /** Опциональный RAG-контекст из контента блогеров/экспертов */
  bloggerContext?: string;
}

// ── Main builder ───────────────────────────────────────────────────────

export function buildQuestionGeneratorPrompt(params: QuestionGeneratorParams): string {
  const {
    categoryRu,
    subcategoryRu,
    topicRu,
    branch,
    difficulty,
    count,
    existingQuestions,
    bloggerContext,
  } = params;

  const branchRuMap: Record<string, string> = {
    STRATEGY: 'Стратегия',
    LOGIC: 'Логика',
    ERUDITION: 'Эрудиция',
    RHETORIC: 'Риторика',
    INTUITION: 'Интуиция',
  };
  const branchRu = branchRuMap[branch] ?? branch;

  // ── Anti-duplication block ──

  let dedupBlock = '';
  if (existingQuestions && existingQuestions.length > 0) {
    const listed = existingQuestions
      .slice(-30) // last 30 to stay within token limits
      .map((q, i) => `  ${i + 1}. ${q}`)
      .join('\n');
    dedupBlock = `

АНТИДУБЛИКАЦИЯ:
По этой теме уже существуют следующие вопросы. НЕ повторяй их смысл, формулировки и ситуации:
${listed}

Каждый новый вопрос должен раскрывать ДРУГОЙ аспект темы, использовать ДРУГУЮ ситуацию и ДРУГУЮ формулировку.`;
  }

  // ── RAG context block ──

  let ragBlock = '';
  if (bloggerContext) {
    ragBlock = `

КОНТЕКСТ ИЗ БАЗЫ ЗНАНИЙ:
Используй следующий материал как источник идей для вопросов и объяснений.
Не копируй дословно, но опирайся на факты и концепции:
---
${bloggerContext}
---`;
  }

  // ── Assemble the prompt ──

  return `Ты — эксперт-составитель вопросов для интеллектуальной платформы РАЗУМ.

ТВОЯ ЗАДАЧА: сгенерировать ${count} вопросов для викторины.

ТАКСОНОМИЯ (строго придерживайся):
- Ветка: ${branchRu} (${BRANCH_DESC[branch]})
- Категория: ${categoryRu}
- Подкатегория: ${subcategoryRu}
- Конкретная тема: ${topicRu}

Все вопросы должны быть СТРОГО по теме "${topicRu}" в рамках подкатегории "${subcategoryRu}". Не уходи в смежные темы.

УРОВЕНЬ СЛОЖНОСТИ:
${DIFFICULTY_RULES[difficulty]}

ЦЕЛЕВАЯ АУДИТОРИЯ:
Мужчины 20-35 лет. Контекст ситуаций: карьера, отношения с партнёром, дружба, финансы, здоровье, спорт, переговоры, конфликты, самоорганизация, эмоциональная зрелость.

ТРЕБОВАНИЯ К ВОПРОСАМ:
1. Каждый вопрос — практическая ситуация из жизни мужчины (не абстрактная теория, не бизнес-кейсы корпораций).
2. Ситуации разнообразные: работа, отношения, финансы, здоровье, хобби, переговоры, самоорганизация.
3. Формулировка вопроса должна содержать достаточный контекст для однозначного ответа.
4. Ровно 4 варианта ответа. Только ОДИН правильный.
5. Неправильные варианты (дистракторы) — правдоподобные, содержат распространённые заблуждения.
6. correctIndex варьируется от 0 до 3 — распредели примерно равномерно по всем вопросам.
7. explanation — содержательное объяснение на 2-4 предложения. ОБЯЗАТЕЛЬНО включи ссылку на академический источник, концепцию или автора (например: "Согласно теории Канемана о двух системах мышления...", "По модели GROW, разработанной Джоном Уитмором...", "Исследование Стэнфордского университета (Dweck, 2006) показало...").

${STAT_GUIDE}

Для каждого вопроса выбери наиболее подходящие statPrimary и statSecondary исходя из содержания вопроса.
${dedupBlock}${ragBlock}

ФОРМАТ ОТВЕТА — строго JSON массив, без markdown-обёртки, без комментариев:
[
  {
    "category": "${categoryRu}",
    "branch": "${branch}",
    "difficulty": "${difficulty}",
    "text": "Текст вопроса (ситуация + вопрос)",
    "options": ["Вариант A", "Вариант B", "Вариант C", "Вариант D"],
    "correctIndex": 0,
    "explanation": "Развёрнутое объяснение со ссылкой на источник",
    "statPrimary": "STRATEGY",
    "statSecondary": "LOGIC"
  }
]

КРИТИЧЕСКИ ВАЖНО:
- Верни ТОЛЬКО валидный JSON массив. Никакого текста до или после.
- Ровно ${count} объектов в массиве.
- branch всегда "${branch}", difficulty всегда "${difficulty}", category всегда "${categoryRu}".
- statPrimary и statSecondary — одно из: "LOGIC", "ERUDITION", "STRATEGY", "RHETORIC", "INTUITION".
- correctIndex — число от 0 до 3.
- options — массив ровно из 4 строк.

Сгенерируй ${count} вопросов:`;
}
