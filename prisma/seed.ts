// ── Main ─────────────────────────────────────────────────────

async function main() {
  console.log("Seeding РАЗУМ database...\n");

  // 1. Admin user
  const admin = await prisma.user.upsert({
    where: { email: "admin@razum.app" },
    update: {},
    create: {
      name: "Admin",
      email: "admin@razum.app",
      role: Role.ADMIN,
      stats: {
        create: {},
      },
    },
  });
  console.log(`✓ Admin user: ${admin.id}`);

  // 2. Questions
  for (const q of questions) {
    await prisma.question.upsert({
      where: { id: q.id },
      update: {},
      create: {
        id: q.id,
        category: q.category,
        branch: q.branch,
        difficulty: q.difficulty,
        text: q.text,
        options: q.options,
        correctIndex: q.correctIndex,
        explanation: q.explanation,
        statPrimary: q.statPrimary,
        statSecondary: q.statSecondary ?? null,
      },
    });
  }
  console.log(`✓ Questions: ${questions.length} seeded`);

  // 3. Modules
  for (const m of modules) {
    await prisma.module.upsert({
      where: {
        branch_orderIndex: {
          branch: m.branch,
          orderIndex: m.orderIndex,
        },
      },
      update: {},
      create: {
        branch: m.branch,
        orderIndex: m.orderIndex,
        title: m.title,
        description: m.description,
        questionIds: m.questionIds,
      },
    });
  }
  console.log(`✓ Modules: ${modules.length} seeded`);

  console.log("\nDone!");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
