const ALL_QUESTIONS: Q[] = [
  ...STRATEGY_DECISION_MAKING,
  ...STRATEGY_LEADERSHIP,
  ...STRATEGY_ENTREPRENEURSHIP,
  ...STRATEGY_TIME_MANAGEMENT,
  ...STRATEGY_FINANCIAL_LITERACY,
];

// ════════════════════════════════════════════════
//  MAIN
// ════════════════════════════════════════════════

async function main() {
  console.log("═══════════════════════════════════════════════");
  console.log("  РАЗУМ — Production Seed v2");
  console.log(`  Всего вопросов: ${ALL_QUESTIONS.length}`);
  console.log("═══════════════════════════════════════════════\n");

  // Дедупликация
  const seen = new Set<string>();
  const unique: Q[] = [];
  for (const q of ALL_QUESTIONS) {
    if (!seen.has(q.text)) {
      seen.add(q.text);
      unique.push(q);
    }
  }

  console.log(`После дедупликации: ${unique.length}\n`);

  // Bulk insert
  const result = await prisma.question.createMany({
    data: unique.map((q) => ({
      id: textToUuid(q.text),
      category: q.category,
      branch: q.branch,
      difficulty: q.difficulty,
      text: q.text,
      options: q.options,
      correctIndex: q.correctIndex,
      explanation: q.explanation,
      statPrimary: q.statPrimary,
      statSecondary: q.statSecondary ?? null,
    })),
    skipDuplicates: true,
  });

  console.log(`Добавлено новых: ${result.count}`);

  // Статистика
  const total = await prisma.question.count();
  console.log(`Итого в БД: ${total}\n`);

  const stats = await prisma.question.groupBy({
    by: ["branch", "difficulty"],
    _count: true,
    where: { isActive: true },
  });
  console.log("Вопросы по branch + difficulty:");
  for (const s of stats) {
    console.log(`  ${s.branch} / ${s.difficulty}: ${s._count}`);
  }

  const catStats = await prisma.question.groupBy({
    by: ["category"],
    _count: true,
    where: { isActive: true },
    orderBy: { _count: { category: "desc" } },
  });
  console.log("\nВопросы по категориям:");
  for (const s of catStats) {
    console.log(`  ${s.category}: ${s._count}`);
  }

  console.log("\n═══════════════════════════════════════════════");
  console.log("  Seed v2 завершён!");
  console.log("═══════════════════════════════════════════════");
}

main()
  .catch((e) => {
    console.error("ОШИБКА:", e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
