import { createHash } from "crypto";

const prisma = new PrismaClient();

function textToUuid(text: string): string {
  const hash = createHash("sha256").update(text).digest("hex");
  return [
    hash.slice(0, 8),
    hash.slice(8, 12),
    "4" + hash.slice(13, 16),
    "8" + hash.slice(17, 20),
    hash.slice(20, 32),
  ].join("-");
}

